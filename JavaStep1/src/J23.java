public class J23 {
	public static void main(String[] args) {
		
		// ★★★★★모양을 5번 찍어 보세요. (다중for문)
		for(int i=0; i<5; i++){
			for(int j=0; j<5; j++){
				System.out.print("★");
		}
		System.out.println();
		}
		
		//----------------------------------------------
		// 삼각형 찍기
		for(int i=0; i<5; i++){
			for(int j=0; j<=i; j++){
				System.out.print("★");
			}
		System.out.println();
	
		}
	}

}

