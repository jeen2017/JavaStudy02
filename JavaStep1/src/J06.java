public class J06 {
	public static void main(String[] args) {
		char[] c;
		c=new char[5];
		c[0]='a';
		c[1]='p';
		c[2]='p';
		c[3]='l';
		c[4]='e';
		System.out.print((char)(c[0]-32)); // A
		System.out.print((char)(c[1]-32)); // P
		System.out.print((char)(c[2]-32)); // P
		System.out.print((char)(c[3]-32)); // L
		System.out.print((char)(c[4]-32)); // E
	}

}
